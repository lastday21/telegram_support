# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ  –°–ö–†–ò–ù–®–û–¢  (Alt+1‚Ä¶9)  ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
def _take_screenshot() -> str:
    with mss() as sct:
        tmp = NamedTemporaryFile(suffix=".png", delete=False)
        sct.shot(output=tmp.name)
    return tmp.name

def _handler(prompt: str):
    tmp = None
    try:
        tmp = _take_screenshot()
        send_photo(tmp, caption=prompt)

        ocr_text = pytesseract.image_to_string(
            Image.open(Path(tmp)), lang="rus+eng", config="--oem 3 --psm 6"
        ).strip()

        print("\n===== AI INPUT =====")
        print(prompt)
        print(ocr_text)
        print("===== END INPUT =====\n")

        answer = solve_image(tmp, prompt)
        send_message(answer)
    except Exception as e:
        send_message(f"–û—à–∏–±–∫–∞ —Å–∫—Ä–∏–Ω-–º–æ–¥—É–ª—è: {e}")
    finally:
        if tmp and Path(tmp).exists():
            Path(tmp).unlink()

